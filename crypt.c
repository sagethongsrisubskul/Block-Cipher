uint64_t textblock_to_hex(char* c){
	union block64 a;

	for(int i=0;i<8;i++){
		a.eighth[7-i]=(0x10*char_to_hex(c[i*2]))+char_to_hex(c[i*2+1]);
	}

	return a.block;
};

uint64_t plaintext_to_hex(char* c){
	union block64 a;

	for(int i=0;i<8;i++){
		a.eighth[7-i]=c[i];
	}

	return a.block;
};

void write_hex_as_chars(FILE* fp,uint64_t a){
	union block64 b;
	b.block=a;

	for(int i=7;i>=0;i--){
		fputc(hex_to_char((b.eighth[i]&0xf0)>>4),fp);
		fputc(hex_to_char(b.eighth[i]&0x0f),fp);
	}
};

void write_hex_as_plaintext(FILE* fp,uint64_t a){
	union block64 b;
	b.block=a;

	for(int i=7;i>=0;i--){
		fputc((int)b.eighth[i],fp);
	}
};

