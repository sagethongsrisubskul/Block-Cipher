uint16_t g(uint16_t a,int t,int rnd){

	uint8_t g1=a >> 8;
	uint8_t g2=a&0xff00 >> 8;

	uint8_t g3=get_ftable(g2 ^ subkeys[0+(4*t)][rnd]) ^ g1;
	uint8_t g4=get_ftable(g3 ^ subkeys[1+(4*t)][rnd]) ^ g2;
	uint8_t g5=get_ftable(g4 ^ subkeys[2+(4*t)][rnd]) ^ g3;
	uint8_t g6=get_ftable(g5 ^ subkeys[3+(4*t)][rnd]) ^ g4;

	//printf("g1=%x g2=%x g3=%x g4=%x g5=%x g6=%x\n",g1,g2,g3,g4,g5,g6);

	union block64 retval;
	retval.block=0x0;
	retval.eighth[0]=g6;
	retval.eighth[1]=g5;

	return retval.fourth[0];
};

