uint16_t get_ftable(uint8_t a){
	uint8_t column=a & 0x0f;
	//printf("column: %x\n",column);
	uint8_t row=(a & 0xf0) >> 4;
	//printf("row: %x\n",row);
	
	return ftable[(16*row)+column];
};

